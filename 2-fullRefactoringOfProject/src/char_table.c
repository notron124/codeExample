u8 SymbolToLCD(u8 sym)
{
   if (sym == ';')
      return 4;
   else if (sym == '[')
      return 0;
   else if (sym == '}')
      return 1;
   else if (sym == '\'')
      return 0;
   else if (sym == '^')
      return 217;
   else if (sym > 191)
      return Symbol[sym - 192];
   else
      return sym;
}
